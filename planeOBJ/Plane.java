package planeOBJ;

public class Plane {


	String modelo;
	String marca;
	String tamanho;
	String cor; 
	String acelera;
	String peso;
	int altitude;
	String perm_voo;	

	public String getmodelo () {
		return modelo;
	}
	public void setmodelo (String modelo) {
		this.modelo = modelo;
	}
	public String getmarca () {
		return marca;
	}
	public void setmarca (String marca) {
		this.marca = marca;
	}
	public String gettipo () {
		return tamanho;
	}
	public void settipo (String tamanho) {
		this.tamanho = tamanho;
	}
	public String getcor () {
		return cor;
	}
	public void setcor (String cor) {
		this.cor = cor;
	}
	public String getacelera () {
		return acelera;
	}
	public void setacelera (String acelera) {
		this.acelera = acelera;
	}
	public String getpeso () {
		return peso;
	}
	public void setpeso (String peso) {
		this.peso = peso;
	}
	public int getalt () {
		return altitude;
	}
	public void setalt (int altitude) {
		this.altitude = altitude;
	}
	public String getperm_voo () {
		return perm_voo;
	}
	public void setperm_voo (String perm_voo) {
		this.perm_voo = perm_voo;
	}

}