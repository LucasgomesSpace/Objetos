package carroOBJ;

public class Carro {



	String modelo;
	String marca;
	String tipo;
	String cor; 
	String acelera;
	String farol;
	String viraL;
	String viraR;

	public String getmodelo () {
		return modelo;
	}
	public void setmodelo (String modelo) {
		this.modelo = modelo;
	}
	public String getmarca () {
		return marca;
	}
	public void setmarca (String marca) {
		this.marca = marca;
	}
	public String gettipo () {
		return tipo;
	}
	public void settipo (String tipo) {
		this.tipo = tipo;
	}
	public String getcor () {
		return cor;
	}
	public void setcor (String cor) {
		this.cor = cor;
	}
	public String getacelera () {
		return acelera;
	}
	public void setacelera (String acelera) {
		this.acelera = acelera;
	}
	public String getfarol () {
		return farol;
	}
	public void setfarol (String farol) {
		this.farol = farol;
	}
	public String getviraL () {
		return viraL;
	}
	public void setviraL (String viraL) {
		this.viraL = viraL;
	}
	public String getviraR () {
		return viraR;
	}
	public void setviraR (String viraR) {
		this.viraR = viraR;
	}

}